#Buat program untuk menerima input berupa dua bilangan dan menghitung hasil penjumlahan, pengurangan, perkalian, dan pembagian dari kedua bilangan tersebut.

bilangan1 = float(input("Masukkan bilangan pertama: "))
bilangan2 = float(input("Masukkan bilangan kedua: "))

penjumlahan = bilangan1 + bilangan2
pengurangan = bilangan1 - bilangan2
perkalian = bilangan1 * bilangan2
pembagian = bilangan1 / bilangan2

print(f"Penjumlahan: {penjumlahan}")
print(f"Pengurangan: {pengurangan}")
print(f"Perkalian: {perkalian}")
print(f"Pembagian: {pembagian}")
